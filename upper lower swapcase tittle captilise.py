#upper:
name="kirankumar"
print(name.upper())   #The upper() method returns a string where all characters are in upper case.
                                # Symbols and Numbers are ignored.
name='kiran1314141kumar'
print(name.upper())

#lower:
name="kirankumar"
print(name.lower())   # Converts a string into lower case  Symbols and Numbers are ignored.

#swapcase:
name="KIrankumAR"
print(name.swapcase())  # in this cass the lower case letters upper case and the upper case letters lower case

#title:
name="kiran kumar"
print(name.title())  #Converts the first character of each word to upper case

#captilise:
name="kiran kumar"
print(name.capitalize()) #Converts the first character to upper case


